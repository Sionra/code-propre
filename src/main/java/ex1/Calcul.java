package ex1;

/**
 * Une class qui définie comment doit ce dérouller une addition et une soustraction
 */
public class Calcul {

    /**
     * Effectue une addition
     *
     * @param a nombre numéro 1 dans l'addition
     * @param b nombre numéro 2 dans l'addition
     * @return
     */
    public int addition(int a, int b) {
        return a + b;
    }

    /**
     * Effectue une soustraction
     *
     * @param a Nombre qui va etre soustrait
     * @param b Nombre qui soustrait le param a
     * @return
     */
    public int soustraction(int a, int b) {
        return a - b;
    }
}
